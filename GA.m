fnc2=10*n+((0.12)^2)-10*cos(2*pi*(0.12))
fnc3=10*n+((3.12)^2)-10*cos(2*pi*(3.12))
fnc4=10*n+((4)^2)-10*cos(2*pi*(4))
fnc_tot=[fnc fnc1 fnc2 fnc3 fnc4]
set(handles.edit26,'String',fnc_tot)

gbin=dec2bin(fnc_tot)
set(handles.edit27,'String',gbin)
ff=max(fnc_tot)
%%%%%%%%%%%%%%%%p and i%%%%%
psi1=fnc/sum(fnc_tot)
psi2=fnc1/sum(fnc_tot)
psi3=fnc2/sum(fnc_tot)
psi4=fnc3/sum(fnc_tot)
psi5=fnc4/sum(fnc_tot)
psi_tot=[psi1 psi2 psi3 psi4 psi5]
set(handles.edit35,'String',psi_tot)
%%%%%%%%%%%%%%%%%%%%%%%%%%%%
avg11=sum(fnc_tot)/length(fnc_tot)
avgtot_b=fnc_tot/avg11
set(handles.edit36,'String',avgtot_b)

%%%%%%%%roulletes wheeling
rw11=round(avgtot_b)
set(handles.edit37,'String',rw11)




%%%%%%%%%%%%%%%%%%%%%%%%%5
[max1, ind1] = max(fnc_tot)
fnc_tot(ind1)      = -Inf;
[max2, ind2] = max(fnc_tot)
fnc_tot(ind2)      = -Inf;

yt=[max1 max2]
set(handles.edit28,'String',yt)
kilo=dec2bin(yt)
set(handles.edit29,'String',kilo)
tot4=get(handles.edit26,'String')
gi=str2num(tot4)
summer1=sum(gi)
set(handles.edit30,'String',summer1)
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
gbin(1,4)='1'
gbin(2,4)='0'
gbin(1,3)='0'
gbin(2,3)='1'
%%%%%%mutation%%%%%%%%%

set(handles.edit32,'String',gbin)
gbin(2,6)='1'
gbin(1,6)='1'
gbin(5,3)='1'
vvb=bin2dec(gbin)

hgi=sum(vvb)
%%%%%%%%%%%%%%%%mutation
gbin(4,3)='1'
cvc=bin2dec(gbin)
sumt=sum(cvc)
set(handles.edit33,'String',hgi)
set(handles.edit34,'String',sumt)
sumfin=[summer1 hgi sumt]
axes(handles.axes3)
plot(sumfin,'r')
grid on





